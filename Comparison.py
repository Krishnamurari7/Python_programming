#Comaprison Operatior always Answer Boolean Format

a = 45
b = 32

print(f"Greater than :-{a > b}")  # Greater than

print(f"Less than :-{a < b}")  # Less than

print(f"Equal to :-{a == b}")  # Equal to

print(f"Not Equal to :-{a != b}")  # Not Equal to

print(f"Greater than or Equal to :-{a >= b}")  # Greater than or Equal to

print(f"Less than or Equal to :-{a <= b}")  # Less than or Equal to

