#Comaprison Operatior always Answer Boolean Format

a = 45
b = 32

print(f"Greater than :-{a > b}")  # Greater than 45 > 32 True

print(f"Less than :-{a < b}")  # Less than  45 < 32 false

print(f"Equal to :-{a == b}")  # Equal to 45 == 32 false

print(f"Not Equal to :-{a != b}")  # Not Equal to 45 != 32 true

print(f"Greater than or Equal to :-{a >= b}")  # Greater than or Equal to 45 >= 32 true

print(f"Less than or Equal to :-{a <= b}")  # Less than or Equal to 45 <= 32 false

